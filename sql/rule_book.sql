-- 规则书内容表
CREATE TABLE IF NOT EXISTS rule_book (
    id INT AUTO_INCREMENT PRIMARY KEY,
    section VARCHAR(50) NOT NULL COMMENT '规则章节: general/ruler/rebel/adventurer/scourge/civilian',
    title VARCHAR(100) NOT NULL COMMENT '规则标题',
    content TEXT COMMENT '规则内容',
    order_num INT NOT NULL DEFAULT 0 COMMENT '排序号',
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX idx_section (section),
    INDEX idx_order (section, order_num)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

